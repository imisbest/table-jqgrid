drop table user;
create table user(
username varchar(99),
truename varchar(99),
password varchar(99),
salt varchar(99),
sex varchar(99)
);

drop table dept;
create table dept(
id VARCHAR(99),
name varchar(99)
);

drop table emp;
create table emp(
id varchar(99),
name varchar(99),
salary varchar(99),
age varchar(99),
bir DATE,
did varchar(99)
);
